a = 2
b = 9

# 算術
print(a + b)
print(a - b)
print(a * b)
print(a ** 2)
print(a ** 0.5)
print(a / b)
print(a % b)


# 比較
print(a > b)
print(a >= b)
print(a < b)
print(a <= b)
print(a == b)

# 指定
# a = b
# a += b => a = a + b
# a -= b => a = a - b
# a *= b => a = a * b
# a /= b => a = a / b
# a %= b => a = a % b